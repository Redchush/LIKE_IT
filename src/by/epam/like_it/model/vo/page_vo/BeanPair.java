package by.epam.like_it.model.vo.page_vo;


public class BeanPair {

    private String key;
    private String value;

    public BeanPair() {}

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
